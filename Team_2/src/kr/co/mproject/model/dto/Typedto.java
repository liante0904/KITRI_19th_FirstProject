package kr.co.mproject.model.dto;

public class Typedto {
	private String type_name;
	private int type_finish;
	private int type_nF;
	private int type_tt;
	public String getType_name() {
		return type_name;
	}
	public void setType_name(String type_name) {
		this.type_name = type_name;
	}
	public int getType_finish() {
		return type_finish;
	}
	public void setType_finish(int type_finish) {
		this.type_finish = type_finish;
	}
	public int getType_nF() {
		return type_nF;
	}
	public void setType_nF(int type_nF) {
		this.type_nF = type_nF;
	}
	public int getType_tt() {
		return type_tt;
	}
	public void setType_tt(int type_tt) {
		this.type_tt = type_tt;
	}
}
